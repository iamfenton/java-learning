public class whileloop {
	public static void main(String[] args) {

		// creating an integer variable called 'num'
		// and giving it the value of '0'.

		int num = 0;

		// while the 'number' variable is less than '6' then
		// write the value of the 'number' variable to the screen
		// and add '1' to the value each time.

		while (num < 6) {
			System.out.println(num); 
			
			// adding '1' to the value of num.

			num++; 
		}
	}
}