public class forloop {
	public static void main(String[] args) {

		// creating an integer variable called number
		// and giving it the value of '0'. for the number
		// being less than '6' add '1' to the number variable
		// each loop and print the variable. 

	for (int number = 0; number < 6; number++) {
			System.out.println(number);
		}
	}
}